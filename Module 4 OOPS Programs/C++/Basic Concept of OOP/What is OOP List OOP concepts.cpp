//OOP stands for Object-Oriented Programming, which is a programming paradigm that organizes software design around objects and data rather than actions and logic. Here are some fundamental concepts of OOP:
//
//1. Objects: Objects are instances of classes that encapsulate data (attributes) and behavior (methods). They are the basic units of OOP.
//
//2. Classes: Classes are blueprints or templates for creating objects. They define the properties (attributes) and behaviors (methods) that objects of the class should have.
//
//3. Encapsulation: Encapsulation is the bundling of data (attributes) and methods that operate on the data into a single unit (class). It hides the internal state of an object from the outside world and only exposes the necessary parts through methods.
//
//4. Inheritance: Inheritance is a mechanism where a class (subclass or derived class) can inherit attributes and behaviors from another class (superclass or base class). It promotes code reusability and allows the creation of hierarchical classifications.
//
//5. Polymorphism: Polymorphism means the ability of objects of different classes to respond to the same message or method call in different ways. It allows methods to be defined in a superclass and overridden by subclasses.
//
//6. Abstraction: Abstraction is the process of identifying essential features of an object and ignoring the non-essential details. It focuses on what an object does rather than how it does it.
//
//7. Modularity: Modularity is the concept of making software components or objects relatively independent, allowing them to be used and maintained in isolation.
//

